package HW6;

public class fifth {
    public static int getSum(int a, int b){
        return a+b;
    }
    public static double getMinus(double a, double b){
        return a-b;
    }
    public static double getDev(double a, double b){
        return a/b;
    }
    public static int getMult(int a, int b){
        return a*b;
    }
    public static void main(String[] args) {
        System.out.println(getSum(10,5));
        System.out.println(getMinus(10,5));
        System.out.println(getDev(10,5));
        System.out.println(getMult(10,5));
    }
}


